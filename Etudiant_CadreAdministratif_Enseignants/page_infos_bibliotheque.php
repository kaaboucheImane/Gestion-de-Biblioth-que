<h2>Informations sur Bibliothéque</h2><hr>
<h3></h3>
<font face="verdana">
<p>Au sein de l'ENS, une bibliothèque centrale et neuf bibliothèques spécialisées situées au niveau des différents départements vous accueillent et vous accompagnent tout au long de votre cursus dans un espace convivial pour mener à bien votre travail documentaire.</p>

<p>Elles ont pour missions :</p>
<p>La mise à disposition du fonds documentaire qui comprend l'ensemble des documents produits en interne (polycopiés, annales, thèses et mémoires), ainsi que les documents achetés, échangés ou reçus à titre de don sur les thématiques de l'école ;</p>
<p>La conservation du fonds avec le recours à la restauration des documents abîmés ;</p>
<p>La communication et le prêt, qui sont les missions essentielles des bibliothèques.</p>
</font>

